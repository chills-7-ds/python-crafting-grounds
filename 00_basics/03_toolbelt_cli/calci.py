def calculator():
    print("\n-- Calculator --")
    try:
        num1 = float(input("Enter first number: "))
        op = input("Enter operator (+, -, *, /): ")
        num2 = float(input("Enter second number: "))

        if op == '+':
            result = num1 + num2
        elif op == '-':
            result = num1 - num2
        elif op == '*':
            result = num1 * num2
        elif op == '/':
            result = num1 / num2
        else:
            result = "Invalid operator."

        print("Result:", result)
    except Exception as e:
        print("Error:", e)
    input("Press Enter to return...")
